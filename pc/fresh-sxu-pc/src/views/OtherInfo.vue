          <p>大学是个小社会，所以多锻炼情商，考虑别人的感受，做到知世故而不世故。比如在寝室里不外放音乐，室友睡觉时轻手轻脚，对不了解的事情缄口不言等等。室友可能是你大学关系最好的朋友，也可能是关系最差的路人，谨慎处理寝室关系。如果关系不好，那就多出去走走，在其他社团里找朋友，别闷在寝室默默生气。</p>

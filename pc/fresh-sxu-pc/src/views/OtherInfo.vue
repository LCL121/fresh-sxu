          <p>山西大学图书馆创建于1902年，始称山西大学堂图书馆，1931年定现名。</p>
          <p>1949年以后，随着山西大学办学规模的不断扩大，图书馆也在不断发展扩建并四迁馆址。1954年建成的图书馆面积为2500平方米(现已拆除建成文科楼)。1959年建成的图书馆（北馆）面积为5400平方米。1995年建成的图书馆，面积16349平方米。位于山西大学新校门内，初民广场的东侧。从1995年建成起一直都是山西大学的标志性建筑，2012年结束了它作为图书馆的使命。</p>
          <p>现在使用的图书馆2009年破土动工开始建设，2013年开馆接待读者。新图书馆结构新颖，色彩鲜明，气势恢弘，传承了山西大学百年传统和悠久文脉，将山西大学传统建筑风格与现代建筑艺术完美结合，成为我校宏伟建筑群的一大亮点，同时她还具有幽雅的人文环境和智能化、人性化，藏、借、阅一体的信息化管理模式，她是山西大学的重要标志；她是顽强拼搏、力争上游的精神象征；也是山西大学的文献信息中心；师生们进行学习和研究的重要场所。</p>
          <p>新图书馆是一个具有较大的空间，统一的柱网，可以灵活变化、以家具组成不同功能的全开放式的建筑。共有四层，每一层又设有不同的功能区。</p>
          <p>全馆共有阅览座位2250个，休闲座位500个，研究、讨论室20多个，还有多个休闲区、视听室、多媒体室，为读者创造了明亮舒适的阅览和娱乐环境。25台服务器、3个磁盘阵列等多种现代化的设备，为读者服务工作提供了坚实的保障。</p>
          <p>截止到2014年，现有纸质文献361.7万册（件），电子图书2535（GB），中外文数据库56个，自建数据库13个，还收藏有一定数量的光盘、缩微资料、视听资料等多种载体文献。山西大学图书馆馆藏线装书1万多种，13万余册，其中不乏孤本与稀见之本。以明清刻本为大宗，其中地方文献和文集尤具特色。2010年6月山西大学图书馆被文化部命名为第三批“全国古籍重点保护单位”。</p>
